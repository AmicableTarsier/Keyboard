static void render_space(void) {
#ifdef WPM_ENABLE
    char wpm = get_current_wpm();
#else
    char wpm = 0;
#endif
    char render_row[128];
    int row,i;

	for (row=0; row<4; row++) {
		oled_set_cursor(0,row);

		unsigned int wpm_shift = wpm/4;
		for(i=0; i<128; i++) {
			unsigned int wpm_offset = i-wpm_shift;
			render_row[i] = pgm_read_byte(pgm_read_ptr(&(space_rows[row]))+i+state);
			if (i>=wpm_shift && wpm_offset < 34) {
				render_row[i] = (render_row[i] & pgm_read_byte(pgm_read_ptr(&(mask_rows[row])) + wpm_offset))
					| pgm_read_byte(pgm_read_ptr(&(ship_rows[row])) + wpm_offset);
			}
		};

		oled_write_raw(render_row, 128);
	}

    state = (state + 1 + (wpm/15)) % (128*2);
	
	oled_render_dirty(true); //call with 'true' for smoother animation, but encoder might skip pulses in this case
}


bool oled_task_user(void) {
    switch (current_oled_state) {
        case OLED_LOGO:
            render_logo();
            if (timer_elapsed(oled_timer) > 3000) {
                current_oled_state = OLED_ANIMATION;
                oled_timer = timer_read();
                current_oled_state = OLED_CLEAR;
            }
            break;
        case OLED_ANIMATION:
            render_space();
            if (timer_elapsed(oled_timer) > 3100) {
                oled_timer = timer_read();
            }
            break;
        case OLED_CLEAR:
            oled_clear();
            if (timer_elapsed(oled_timer) > 3000) {
                current_oled_state = OLED_ANIMATION;
                oled_timer = timer_read();
            }
            break;
        case OLED_STARTUP:
        default:
            if (timer_elapsed(oled_timer) > 3000) {
                current_oled_state = OLED_CLEAR;
                oled_timer = timer_read();
            }
            break;
    };
    oled_render_dirty(true);
    return false;
}
